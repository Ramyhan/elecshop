    PINFO1 NVARCHAR2(50),
    PINFO2 NVARCHAR2(50),
    PINFO3 NVARCHAR2(50),
    PIMAGE_THOUMB NVARCHAR2(50),
    PIMAGE_INFO1 NVARCHAR2(50),
    PIMAGE_INFO2 NVARCHAR2(50),
    PREGDATE DATE DEFAULT SYSDATE
);

