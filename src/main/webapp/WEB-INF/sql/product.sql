CREATE TABLE TBL_PRODUCT(
    PNO NUMBER CONSTRAINT PK_PNO PRIMARY KEY,
    PNAME NVARCHAR2(50) NOT NULL,
    PPRICE NUMBER NOT NULL,
    PCODE NVARCHAR2(50),
    PTYPE NUMBER(1) NOT NULL, -- 노트북:1, 모니터:2, 태블릿:3, 음향기기:4, 주변기기:5
